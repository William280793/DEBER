public class FIGURA {
     private double base;
     private double altura;
     
    public FIGURA() {
    }

    public FIGURA(int base) {
        this.base = base;
    }

    public FIGURA(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }

    public double getBase() {
        return base;
    }

    public void setBase(double base) {
        this.base = base;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }
     
     
     
     

   
  
    
}
